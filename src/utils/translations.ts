export const t = (
  key: TranslationKey,
  lang: string,
  variables?: Record<string, string | number>
): string => {
  const dictionary = translations[lang] || translations.en; // Fallback to English if lang not found
  let text = dictionary[key] || translations.en[key] || String(key);

  if (variables) {
    Object.entries(variables).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }

  return text;
};
